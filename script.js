let currentFlagIndex = 0;
let score = 0;
let shuffledFlags = [...flags]; // Create a shuffled copy of the flags array

function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array; // Add this line to return the shuffled array
}

function shuffleFlags() {
    shuffledFlags = [...flags]; // Reset shuffledFlags to the original array
    shuffleArray(shuffledFlags); // Shuffle the flags array
}

function updateTotalFlags() {
    const totalFlags = flags.length;
    document.getElementById('total-flags').textContent = `Total: ${currentFlagIndex + 1} / ${totalFlags}`;
}

function loadFlag() {
    if (currentFlagIndex < shuffledFlags.length) {
        const flag = shuffledFlags[currentFlagIndex];
        document.getElementById('flag').src = flag.flagPath;
        document.getElementById('hint').innerHTML = `<strong>Capital:</strong> ${flag.capital}, <strong>Continent:</strong> ${flag.continent}`;
        const options = shuffleArray([flag.correctAnswer, ...flag.wrongAnswers]);
        options.forEach((option, index) => {
            document.getElementsByClassName('option')[index].textContent = option;
        });

        // Update the total flag count
        updateTotalFlags();
    } else {
        endGame();
    }
}

function checkAnswer(button) {
    const selectedAnswer = button.textContent;
    const flag = shuffledFlags[currentFlagIndex];
    const hintElement = document.getElementById('hint');

    if (selectedAnswer === flag.correctAnswer) {
        score++;
        hintElement.textContent = 'Correct!';
        hintElement.style.color = 'green'; // Set text color to green for correct answer
    } else {
        hintElement.textContent = `Answer: ${flag.correctAnswer}`;
        hintElement.style.color = 'red'; // Set text color to red for wrong answer
    }

    currentFlagIndex++;

    // Update the score display
    const totalFlags = flags.length;
    document.getElementById('score').textContent = `Score: ${score} / ${totalFlags}`;

    setTimeout(() => {
        hintElement.textContent = ''; // Clear the message
        hintElement.style.color = ''; // Reset text color
        loadFlag();
    }, 1600);
}

function startGame() {
    currentFlagIndex = 0;
    score = 0;
    document.getElementById('score').textContent = 'Score: 0';
    document.getElementById('hint').textContent = '';
    document.getElementById('flag').style.display = 'block';
    document.getElementById('options').style.display = 'block';
    document.getElementById('play-again').style.display = 'none';

    shuffleFlags(); // Shuffle the flags array
    loadFlag(); // Load the first flag
}

function endGame() {
    const totalFlags = flags.length;
    const scoreText = `Score: ${score} / ${totalFlags}`;
    document.getElementById('hint').textContent = 'Game Over!';
    document.getElementById('score').textContent = scoreText;
    document.getElementById('flag').style.display = 'none'; // Hide the flag
    document.getElementById('options').style.display = 'none';
    document.getElementById('play-again').style.display = 'block'; // Show the Play Again button
}

function playAgain() {
    currentFlagIndex = 0;
    score = 0;
    document.getElementById('score').textContent = 'Score: 0';
    document.getElementById('hint').textContent = '';
    document.getElementById('flag').style.display = 'block';
    document.getElementById('options').style.display = 'block';
    document.getElementById('play-again').style.display = 'none';

    shuffleFlags(); // Shuffle the flags array
    loadFlag(); // Load the first flag
}

// Initialize the total flag count on window load
window.onload = function () {
    updateTotalFlags();
    startGame();
}

